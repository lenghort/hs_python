import turtle
turtle.speed(1)
lenght = 300
degrees = 144

turtle.color("red")
turtle.begin_fill()
turtle.forward(lenght)
turtle.right(degrees)
turtle.forward(lenght)
turtle.right(degrees)
turtle.forward(lenght)
turtle.right(degrees)
turtle.forward(lenght)
turtle.right(degrees)
turtle.forward(lenght)
turtle.end_fill()

turtle.mainloop()